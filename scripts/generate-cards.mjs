const minors = [];
suits.forEach(s => {
  ranks.forEach((r, i) => {
    const id = `${s.suit}-${String(r.num).padStart(2,'0')}`;
    const upGen = suitMeanings[s.suit].uprights[i] || '';
    const revGen = suitMeanings[s.suit].reversed[i] || '';
    minors.push({
      id, name_zh: s.zh + r.zh, name_en: `${r.en} of ${s.suit.charAt(0).toUpperCase()+s.suit.slice(1)}`,
      arcana: 'minor', suit: s.suit, number: r.num, element: s.element,
      keywords_zh: r.kw, emoji: s.emoji,
      description: `${s.zh}${r.zh}——小阿卡纳${s.zh}牌组，代表${r.kw.join('、')}。`,
      upright: { general: upGen, love: upGen, study: upGen, work: upGen, travel: upGen, social: upGen },
      reversed: { general: revGen, love: revGen, study: revGen, work: revGen, travel: revGen, social: revGen }
    });
  });
});

const all = [...major, ...minors];
writeFileSync(resolve(dataDir, 'tarot-cards.json'), JSON.stringify(all), 'utf8');
